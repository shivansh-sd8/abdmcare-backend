function buildStaffCollectionsSheet(wb: ExcelJS.Workbook, r: HospitalReport) {
  // One row per staff member who collected anything in the window. The
  // per-method amounts are flattened into wide CASH/UPI/CARD/BANK columns
  // so the analyst can sort/sum without having to pivot a long table.
  const sheet = setupSheet(wb, 'Staff Collections', [
    { header: 'Collector', key: 'name', width: 28 },
    { header: 'Role', key: 'role', width: 16 },
    { header: 'Email', key: 'email', width: 32 },
    { header: 'Receipts', key: 'count', width: 12, numFmt: '0' },
    { header: 'Total INR', key: 'total', width: 16, numFmt: '#,##0.00' },
    { header: 'Cash', key: 'cash', width: 14, numFmt: '#,##0.00' },
    { header: 'UPI', key: 'upi', width: 14, numFmt: '#,##0.00' },
    { header: 'Card', key: 'card', width: 14, numFmt: '#,##0.00' },
    { header: 'Bank', key: 'bank', width: 14, numFmt: '#,##0.00' },
    { header: 'Other', key: 'other', width: 14, numFmt: '#,##0.00' },
    { header: 'First collected', key: 'first', width: 22 },
    { header: 'Last collected', key: 'last', width: 22 },
  ]);
  applyHeaderStyle(sheet);
  for (const s of r.billing.byStaff) {
    const m = (key: string) =>
      s.byMethod.find((b) => b.method === key)?.amount || 0;
    sheet.addRow({
      name: s.name,
      role: s.role || '—',
      email: s.email || '—',
      count: s.paymentCount,
      total: s.total,
      cash: m('CASH'),
      upi: m('UPI'),
      card: m('CARD'),
      bank: m('BANK_TRANSFER'),
      other: s.byMethod
        .filter((x) => !['CASH', 'UPI', 'CARD', 'BANK_TRANSFER'].includes(x.method))
        .reduce((acc, x) => acc + x.amount, 0),
      first: s.firstCollectedAt ? new Date(s.firstCollectedAt).toLocaleString('en-IN', { timeZone: 'Asia/Kolkata' }) : '—',
      last: s.lastCollectedAt ? new Date(s.lastCollectedAt).toLocaleString('en-IN', { timeZone: 'Asia/Kolkata' }) : '—',
    });
  }
  applyZebra(sheet);
}

