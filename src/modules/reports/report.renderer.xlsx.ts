import ExcelJS from 'exceljs';
import { Writable } from 'stream';
import { HospitalReport } from './report.types';

/**
 * Multi-sheet Excel workbook for the hospital report. The structure mirrors
 * the PDF: every section gets its own sheet so analysts can pivot/sort the
 * raw rows. Streamed via ExcelJS's `xlsx.write(stream)` so we don't have to
 * materialise the entire workbook in memory.
 *
 * Convention: numeric columns get number formats applied at the column
 * level (rather than per-cell) so Excel interprets them as numbers and the
 * sums on a column actually work for the user.
 */

const HEADER_FILL: ExcelJS.FillPattern = {
  type: 'pattern',
  pattern: 'solid',
  fgColor: { argb: 'FF0F766E' },
};

const HEADER_FONT: Partial<ExcelJS.Font> = {
  bold: true,
  color: { argb: 'FFFFFFFF' },
  size: 11,
};

const ZEBRA_FILL: ExcelJS.FillPattern = {
  type: 'pattern',
  pattern: 'solid',
  fgColor: { argb: 'FFF8FAFC' },
};

interface ColSpec {
  header: string;
  key: string;
  width?: number;
  numFmt?: string;
}

function applyHeaderStyle(sheet: ExcelJS.Worksheet) {
  const row = sheet.getRow(1);
  row.eachCell((cell) => {
    cell.fill = HEADER_FILL;
    cell.font = HEADER_FONT as ExcelJS.Font;
    cell.alignment = { vertical: 'middle', horizontal: 'left' };
    cell.border = {
      bottom: { style: 'thin', color: { argb: 'FF0F766E' } },
    };
  });
  row.height = 22;
  sheet.views = [{ state: 'frozen', ySplit: 1 }];
}

function applyZebra(sheet: ExcelJS.Worksheet) {
  for (let i = 2; i <= sheet.rowCount; i++) {
    if (i % 2 === 0) {
      const row = sheet.getRow(i);
      row.eachCell({ includeEmpty: true }, (cell) => {
        cell.fill = ZEBRA_FILL;
      });
    }
  }
}

function setupSheet(wb: ExcelJS.Workbook, name: string, cols: ColSpec[]): ExcelJS.Worksheet {
  // Excel sheet names are limited to 31 chars; clip safely.
  const sheet = wb.addWorksheet(name.substring(0, 31));
  sheet.columns = cols.map((c) => ({
    header: c.header,
    key: c.key,
    width: c.width || 18,
    style: c.numFmt ? { numFmt: c.numFmt } : undefined,
  }));
  return sheet;
}

export function renderHospitalReportXlsx(report: HospitalReport, output: Writable): Promise<void> {
  const wb = new ExcelJS.Workbook();
  wb.creator = 'AbhaAyushman';
  wb.created = new Date();
  wb.lastModifiedBy = report.header.generatedBy.name;

  buildSummarySheet(wb, report);
  buildPatientsSheet(wb, report);
  buildEncountersSheet(wb, report);
  buildDoctorsSheet(wb, report);
  buildDoctorTrendsSheet(wb, report);
  buildAppointmentsSheet(wb, report);
  buildIpdSheet(wb, report);
  buildPharmacySheet(wb, report);
  buildLabSheet(wb, report);
  buildBillingSheet(wb, report);
  buildStaffCollectionsSheet(wb, report);
  buildAbdmSheet(wb, report);

  return wb.xlsx.write(output as any).then(() => undefined);
}

