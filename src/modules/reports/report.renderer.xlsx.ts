function buildDoctorTrendsSheet(wb: ExcelJS.Workbook, r: HospitalReport) {
  const sheet = setupSheet(wb, 'Doctor Trends', [
    { header: 'Doctor', key: 'name', width: 26 },
    { header: 'Cadence', key: 'cadence', width: 12 },
    { header: 'Bucket', key: 'bucket', width: 22 },
    { header: 'Patients', key: 'count', width: 12, numFmt: '0' },
  ]);
  applyHeaderStyle(sheet);
  for (const d of r.doctors) {
    for (const x of d.trends.daily) {
      sheet.addRow({ name: d.name, cadence: 'Daily', bucket: `${x.label} ${x.date.substring(0, 10)}`, count: x.count });
    }
    for (const x of d.trends.weekly) {
      sheet.addRow({ name: d.name, cadence: 'Weekly', bucket: `Week of ${x.weekStart}`, count: x.count });
    }
    for (const x of d.trends.monthly) {
      sheet.addRow({ name: d.name, cadence: 'Monthly', bucket: x.month, count: x.count });
    }
    for (const x of d.trends.yearly) {
      sheet.addRow({ name: d.name, cadence: 'Yearly', bucket: x.year, count: x.count });
    }
  }
  applyZebra(sheet);
}

