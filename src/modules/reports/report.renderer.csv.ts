function buildEncountersCsv(r: HospitalReport): string {
  const headers = ['Section', 'Bucket', 'Count'];
  const rows: Array<Record<string, unknown>> = [];
  rows.push({ Section: 'TOTAL', Bucket: 'Encounters in range', Count: r.encounters.totalInRange });
  rows.push({ Section: 'TOTAL', Bucket: 'Average per day', Count: r.encounters.avgPerDay });
  for (const t of r.encounters.byType) rows.push({ Section: 'BY TYPE', Bucket: t.type, Count: t.count });
  for (const s of r.encounters.byStatus) rows.push({ Section: 'BY STATUS', Bucket: s.status, Count: s.count });
  for (const d of r.encounters.dailyTrend) {
    rows.push({ Section: 'DAILY', Bucket: `${d.label} ${d.date.substring(0, 10)}`, Count: d.count });
  }
  return toCsv(headers, rows);
}

