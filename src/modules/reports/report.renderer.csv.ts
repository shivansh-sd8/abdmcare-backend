function buildDoctorTrendsCsv(r: HospitalReport): string {
  const headers = ['Doctor', 'Cadence', 'Bucket', 'Patients'];
  const rows: Array<Record<string, unknown>> = [];
  for (const d of r.doctors) {
    for (const x of d.trends.daily) rows.push({ Doctor: d.name, Cadence: 'Daily', Bucket: `${x.label} ${x.date.substring(0, 10)}`, Patients: x.count });
    for (const x of d.trends.weekly) rows.push({ Doctor: d.name, Cadence: 'Weekly', Bucket: `Week of ${x.weekStart}`, Patients: x.count });
    for (const x of d.trends.monthly) rows.push({ Doctor: d.name, Cadence: 'Monthly', Bucket: x.month, Patients: x.count });
    for (const x of d.trends.yearly) rows.push({ Doctor: d.name, Cadence: 'Yearly', Bucket: x.year, Patients: x.count });
  }
  return toCsv(headers, rows);
}

