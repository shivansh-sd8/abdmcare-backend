function buildStaffCollectionsCsv(r: HospitalReport): string {
  // Wide layout (one row per collector, methods as columns) is friendlier
  // for spreadsheet pivots than long format. Methods we don't recognise
  // collapse into "Other" so the row total still reconciles.
  const headers = [
    'Collector', 'Role', 'Email', 'Receipts', 'Total INR',
    'Cash', 'UPI', 'Card', 'Bank', 'Other',
    'First collected', 'Last collected',
  ];
  const m = (s: typeof r.billing.byStaff[number], key: string) =>
    s.byMethod.find((b) => b.method === key)?.amount || 0;
  const rows = r.billing.byStaff.map((s) => ({
    Collector: s.name,
    Role: s.role || '',
    Email: s.email || '',
    Receipts: s.paymentCount,
    'Total INR': s.total,
    Cash: m(s, 'CASH'),
    UPI: m(s, 'UPI'),
    Card: m(s, 'CARD'),
    Bank: m(s, 'BANK_TRANSFER'),
    Other: s.byMethod
      .filter((x) => !['CASH', 'UPI', 'CARD', 'BANK_TRANSFER'].includes(x.method))
      .reduce((acc, x) => acc + x.amount, 0),
    'First collected': s.firstCollectedAt || '',
    'Last collected': s.lastCollectedAt || '',
  }));
  return toCsv(headers, rows);
}

